#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    Plus,
    Minus,
    Star,
    Slash,
    Eq,
    Neq,
    Gte,
    Lte,
    Gt,
    Lt,
    Identifier(String),
    Number(i64),
    StringLiteral(String),
    Keyword(Keyword),
    Comma,
    Dot,
    LParen,
    RParen,
    Semicolon,
    Eof,
}

#[derive(Debug, Clone, PartialEq)]
pub enum Keyword {
    Select,
    From,
    Where,
    Order,
    By,
    Create,
    Table,
    Int,
    Varchar,
    Check,
    Not,
    Null,
    Primary,
    Key,
    Foreign,
    References,
    As,
}